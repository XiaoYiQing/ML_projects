
# Targeted continuous array entries access
b = "Hello, World!"
print(b[2:5])

# Target array entries up to specified index, not including the specified index.
b = "Hello, World!"
print(b[:5])

# Target array entries from specified index to the end, inclusively.
b = "Hello, World!"
print(b[2:])

# Negative array indexing.
b = "9876543210"
print(b[-4], b[-1])

# Negative array indexing slicing. 
b = "9876543210"
print(b[-4:-1])